# What if

people = 20
cats = 30
puppies = 15

if people < cats:
    print("Too many cats! The world is doomed!")
    
if people > cats:
    print("Not many cats! The world is saved!")

if people < puppies:
    print("The world is dry!")

if people > puppies:
    print("Not enough cuteness to go around")


puppies += 5
if people >= puppies:
    print("People are greater than or equal to puppies.")

if people <= puppies:
    print("People are less than or equal to puppies.")

if people == puppies:
    print("People are puppies.")
